    <script>
    function confirmPublish(courseId) {
        Swal.fire({
            title: 'Publish Course?',
            text: "This course will be visible to all students!",
            icon: 'question',
            showCancelButton: true,
            confirmButtonColor: '#10B981',
            cancelButtonColor: '#6B7280',
            confirmButtonText: 'Yes, publish it!',
            cancelButtonText: 'Cancel'
        }).then((result) => {
            if (result.isConfirmed) {
                const form = document.createElement('form');
                form.method = 'POST';
                form.action = '/Youdemy/controllers/admin/update_course_status.php';
                
                const courseIdInput = document.createElement('input');
                courseIdInput.type = 'hidden';
                courseIdInput.name = 'course_id';
                courseIdInput.value = courseId;
                
                const statusInput = document.createElement('input');
                statusInput.type = 'hidden';
                statusInput.name = 'status';
                statusInput.value = 'published';
                
                form.appendChild(courseIdInput);
                form.appendChild(statusInput);
                document.body.appendChild(form);
                form.submit();
            }
        });
    }

    function confirmUnpublish(courseId) {
        Swal.fire({
            title: 'Unpublish Course?',
            text: "This course will no longer be visible to students!",
            icon: 'warning',
            showCancelButton: true,
            confirmButtonColor: '#EAB308',
            cancelButtonColor: '#6B7280',
            confirmButtonText: 'Yes, unpublish it!',
            cancelButtonText: 'Cancel'
        }).then((result) => {
            if (result.isConfirmed) {
                const form = document.createElement('form');
                form.method = 'POST';
                form.action = '/Youdemy/controllers/admin/update_course_status.php';
                
                const courseIdInput = document.createElement('input');
                courseIdInput.type = 'hidden';
                courseIdInput.name = 'course_id';
                courseIdInput.value = courseId;
                
                const statusInput = document.createElement('input');
                statusInput.type = 'hidden';
                statusInput.name = 'status';
                statusInput.value = 'draft';
                
                form.appendChild(courseIdInput);
                form.appendChild(statusInput);
                document.body.appendChild(form);
                form.submit();
            }
        });
    }

    function confirmDelete(courseId) {
        Swal.fire({
            title: 'Delete Course?',
            text: "You won't be able to revert this!",
            icon: 'warning',
            showCancelButton: true,
            confirmButtonColor: '#EF4444',
            cancelButtonColor: '#6B7280',
            confirmButtonText: 'Yes, delete it!',
            cancelButtonText: 'Cancel'
        }).then((result) => {
            if (result.isConfirmed) {
                const form = document.createElement('form');
                form.method = 'POST';
                form.action = '/Youdemy/controllers/admin/delete_course.php';
                
                const input = document.createElement('input');
                input.type = 'hidden';
                input.name = 'course_id';
                input.value = courseId;
                
                form.appendChild(input);
                document.body.appendChild(form);
                form.submit();
            }
        });
    }
